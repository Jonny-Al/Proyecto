public class CImplementos {

    private int idobjeto;
    private String objetonombre, marca, serial, caracteristicas, estado, nombreevento;

    public int getIdobjeto() {
        return idobjeto;
    }

    public void setIdobjeto(int idobjeto) {
        this.idobjeto = idobjeto;
    }

    public String getObjetonombre() {
        return objetonombre;
    }

    public void setObjetonombre(String objetonombre) {
        this.objetonombre = objetonombre;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getSerial() {
        return serial;
    }

    public void setSerial(String serial) {
        this.serial = serial;
    }

    public String getCaracteristicas() {
        return caracteristicas;
    }

    public void setCaracteristicas(String caracteristicas) {
        this.caracteristicas = caracteristicas;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getNombreevento() {
        return nombreevento;
    }

    public void setNombreevento(String nombreevento) {
        this.nombreevento = nombreevento;
    }

}
